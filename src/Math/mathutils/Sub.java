package Math.mathutils;

public class Sub {
    public static int sub(int a, int b) {
        return a - b;
    }

    public static double sub(double a, double b) {
        return a - b;
    }

    public static double sub(int a, double b) {
        return a - b;
    }

    public static double sub(double a, int b) {
        return a - b;
    }
}
