package Math.mathutils;

public class Mul {
    public static int mul(int a, int b) {
        return a * b;
    }

    public static double mul(double a, double b) {
        return a * b;
    }

    public static double mul(int a, double b) {
        return a * b;
    }

    public static double mul(double a, int b) {
        return a * b;
    }
}
